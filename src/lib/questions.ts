const PART_ORDER: Record<DemoQuestion["part"], number> = {
  "Part 1": 1,
  "Part 2": 2,
  "Part 3": 3,
};

export const orderedQuestions: DemoQuestion[] = questions
  .map((question, index) => ({ question, index }))
  .sort((left, right) => {
    const byPart = PART_ORDER[left.question.part] - PART_ORDER[right.question.part];

    if (byPart !== 0) {
      return byPart;
    }

    return left.index - right.index;
  })
  .map(({ question }) => question);

export function getQuestionBySlug(slug: string) {
  return questions.find((question) => question.slug === slug);
}

export function getQuestionById(id: string) {
  return questions.find((question) => question.id === id);
}
